void qcom_ufs_dev_reset(void)
{
	write32((void *)TLMM_UFS_DEV_RESET, UFS_DEV_RESET_ASSERT);
	udelay(10);
	write32((void *)TLMM_UFS_DEV_RESET, UFS_DEV_RESET_DEASSERT);
	udelay(10);
}

void qcom_ufs_setup_mux(void)
{
	write32((void *)TCSR_PCIE6_UFS_MUX_SEL, 1);
}

